#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*ptr;
	size_t			ind;

	ind = 0;
	ptr = s;
	while (ind < n)
	{
		*ptr++ = '\0';
		ind++;
	}
}
